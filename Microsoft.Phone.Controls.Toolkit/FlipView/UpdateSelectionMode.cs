namespace Microsoft.Phone.Controls
{
    /// <summary>
    /// Indicates which condition should cause a <see cref="T:Microsoft.Phone.Controls.FlipView" /> to update its selection.
    /// </summary>
    public enum UpdateSelectionMode
    {
        /// <summary>
        /// Updates selection after transition animation.
        /// </summary>
        AfterTransition,
        /// <summary>
        /// Updates selection before transition animation.
        /// </summary>
        BeforeTransition
    }
}
